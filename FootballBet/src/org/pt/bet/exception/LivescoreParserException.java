package org.pt.bet.exception;

public class LivescoreParserException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public LivescoreParserException() {
		super();
	}

	public LivescoreParserException(String message, Throwable cause) {
		super(message, cause);
	}

	public LivescoreParserException(String message) {
		super(message);
	}

	public LivescoreParserException(Throwable cause) {
		super(cause);
	}

}
